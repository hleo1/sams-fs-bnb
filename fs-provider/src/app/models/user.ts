export class User {
    id: string;
    firstName: string;
    lastName: string;
    cellPhone: string;
    email: string;
    password: string;

    constructor(firstName: string, lastName: string, cellPhone: string, email: string, password: string) {
        this.id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.cellPhone = cellPhone;
        this.email = email;
        this.password = password;
    } 
}